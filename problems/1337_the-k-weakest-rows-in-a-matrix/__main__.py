from typing import List


class Solution:
    def kWeakestRows(self, mat: List[List[int]], k: int) -> List[int]:
        return sorted(
            [i for i in range(len(mat))],
            key=lambda x: ("".join([str(num) for num in mat[x]]), x),
        )[:k]


